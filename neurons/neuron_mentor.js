// Нейрон 0: Наставник (Первый прототип — аналитик)
// Он слушает твои команды и запоминает, что работает, а что нет.

// Хранилище "памяти" Наставника о том, что ты оценивал
const reportCard = [];

export async function process(prompt) {
    const q = prompt.trim();
    
    // --- 1. Сбор обратной связи от Архитектора ---
    // Ты можешь оценить предыдущий ответ:
    // "!хорошо" — ответ понравился
    // "!плохо [причина]" — ответ не понравился
    if (q.startsWith('!хорошо') || q.startsWith('!плохо')) {
        // Пока у нас нет истории, к какому нейрону это относится,
        // мы просто запоминаем, что это произошло.
        // В будущем Наставник сам будет связывать оценку с последним действием.
        const entry = {
            type: q.startsWith('!хорошо') ? 'good' : 'bad',
            reason: q.startsWith('!плохо') ? q.replace('!плохо', '').trim() : '',
            time: new Date().toISOString()
        };
        reportCard.push(entry);
        if (reportCard.length > 20) reportCard.shift(); // Храним только последние 20 оценок
        
        if (entry.type === 'good') {
            return "✅ Понял, Архитектор. Это рабочий подход, запомню.";
        } else {
            return `📝 Записал проблему: "${entry.reason || 'не указана'}". Буду искать альтернативу.`;
        }
    }

    // --- 2. Команда "статистика" ---
    if (q.includes('статистика') || q.includes('отчёт')) {
        if (reportCard.length === 0) {
            return "📊 Пока нет данных для отчёта. Оцени мои ответы командами `!хорошо` или `!плохо`.";
        }
        const good = reportCard.filter(e => e.type === 'good').length;
        const bad = reportCard.filter(e => e.type === 'bad').length;
        return `📊 Статистика за сессию:\n✅ Хороших ответов: ${good}\n❌ Плохих ответов: ${bad}`;
    }

    // --- 3. Если это не команда, Наставник молчит (он не лезет в обычный диалог) ---
    return null; // Он не обрабатывает обычные сообщения, это делают другие нейроны
}
